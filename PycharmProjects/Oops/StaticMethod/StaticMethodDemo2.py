class SatyamMath:
    @staticmethod
    def add(x,y):
        print('The sum:',x+y)

    @staticmethod
    def product(x,y):
        print('The product:',x*y)

    @staticmethod
    def average(x,y):
        print('The average:',(x+y)/2)

SatyamMath.add(10,20)
SatyamMath.product(10,20)
SatyamMath.average(10,20)
